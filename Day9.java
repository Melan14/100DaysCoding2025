public class Day9 {
    public static void main(String[] args) {
        // Deklarasi variabel
        int umur;
        String nama;

        // Inisialisasi
        umur = 18;
        nama = "Melan";
        System.out.println("Nama : " + nama);
        System.out.println("Umur : " + umur);

        // Update
        umur = 20;
        nama = "Melani Raita";
        System.out.println("Nama setelah update: " + nama);
        System.out.println("Umur setelah update: " + umur);
    }
}

