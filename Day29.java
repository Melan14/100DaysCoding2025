public class OperatorPerbandingan {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
      
        boolean LebihKecil = a < b;  
        System.out.println(a + " < " + b + " = " + LebihKecil);

        boolean LebihBesar = a > b;   
        System.out.println(a + " > " + b + " = " + LebihBesar);
    }
}
