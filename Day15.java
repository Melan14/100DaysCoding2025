public class Day15 {
        public static void main(String[] args) {
            int a = 20;
            int b = 10;

            int tambah = a + b;
            int kurang = b - a;

            System.out.println("a = " + a);
            System.out.println("b = " + b);
            System.out.println("Hasil Penjumlahan (a + b) = " + tambah);
            System.out.println("Hasil Pengurangan (a - b) = " + kurang);
    }

}
