public class Day4 {
    public static void main(String[] args) {
        // 1. \n = baris baru
        System.out.println("Halo!\nSelamat datang di Java.");

        // 2. \t = tab
        System.out.println("Nama:\tMelani\tRaita");

        // 3. \b = backspace (hapus karakter sebelumnya)
        System.out.println("Belajarr\b Java"); // huruf 'r' terakhir dihapus

        // 4. \r = carriage return (kursor ke awal baris)
        System.out.println("Halo Dunia!\rJava");
        // Output: "Java Dunia!" (Halo diganti oleh Java)

        // 5. \' = tanda kutip tunggal
        System.out.println("Contoh: \'A\' adalah huruf pertama.");

        // 6. \" = tanda kutip ganda
        System.out.println("Quotes favorit: \"Programming is not about typing, it's about thinking.\"");

        // 7. \\ = backslash
        System.out.println("Folder ada di C:\\Users\\Melani\\Documents");
    }
}
