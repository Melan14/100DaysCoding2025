public class Day30 {
        public static void main(String[] args) {
            int angka1 = 10;
            int angka2 = 15;

            // Operator >= (lebih besar sama dengan)
            boolean BesarSama = angka1 >= angka2;
            System.out.println(angka1 + " >= " + angka2 + " = " + BesarSama);

            // Operator <= (lebih kecil sama dengan)
            boolean KecilSama = angka1 <= angka2;
            System.out.println(angka1 + " <= " + angka2 + " = " + KecilSama);

    }
}
