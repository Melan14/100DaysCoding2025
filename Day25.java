public class Day25 {
    public static void main(String[] args) {
        int angka = 10;

        System.out.println("Nilai awal : " + angka);

        angka++; // increment
        System.out.println("Setelah ++ : " + angka);
        angka--; // decrement
        System.out.println("Setelah -- : " + angka);
    }
}
